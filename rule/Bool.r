

let Bool = { True, False }

let and(Bool,Bool) -> Bool : (True, True) -> True, (True, False) -> False, (False, True) -> False, (False, False) -> False

let or(Bool,Bool) -> Bool : (True, True) -> True, (True, False) -> True, (False, True) -> True, (False, False) -> False

let not(Bool) -> Bool : True -> False, False -> True




